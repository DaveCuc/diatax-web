# =========================================================================
# Endpoint: Session ZIP Download and Sandbox Cleanup (/download)
# =========================================================================
# Serves the compiled documentation.zip file.
# Spawns a FastAPI BackgroundTask that safely and forcefully deletes
# the temporary workspace directory immediately after dispatch completion.
# =========================================================================
@app.get("/download/{session_id}")
async def download_session_zip(session_id: str, background_tasks: BackgroundTasks):
    """
    Serves the output documentation.zip for the given session ID.
    Queues a background cleanup task to wipe the local sandbox files.
    """
    from app.agent import rmtree_force
    zip_path = Path("workspace_tmp") / session_id / "documentation.zip"
    
    if not zip_path.exists():
        from fastapi import HTTPException
        raise HTTPException(status_code=404, detail="The file does not exist or has already been deleted.")
        
    def cleanup():
        import time
        time.sleep(2)  # Delay buffer to guarantee the FileResponse transfer has completed
        rmtree_force(Path("workspace_tmp") / session_id)
        
    # Queue the cleanup task to execute in the background after returning response
    background_tasks.add_task(cleanup)
    
    return FileResponse(
        path=zip_path,
        filename="documentation.zip",
        media_type="application/zip"
    )

