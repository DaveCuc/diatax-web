# =========================================================================
# Endpoint: Trigger Documentation Generation (/generate)
# =========================================================================
# Handles incoming client requests, spawns the InMemoryRunner asynchronously,
# executes the multi-agent graph workflow, and returns the session token.
# =========================================================================
from pydantic import BaseModel

class GenerateRequest(BaseModel):
    repo_url: str
    guide_type: str
    description: str

@app.post("/generate")
async def generate_documentation_endpoint(req: GenerateRequest) -> dict[str, str]:
    """
    Receives GitHub repository URL, description, and guide type.
    Fires the ADK 2.0 Graph Workflow nodes asynchronously and returns
    the generated session UUID to enable subsequent file downloads.
    """
    runner = app.state.runner
    
    # Setup session inputs
    payload = {
        "repo_url": req.repo_url,
        "guide_type": req.guide_type,
        "description": req.description
    }
    
    # Create session using the pre-configured runner
    session = await runner.session_service.create_session(
        app_name=app.state.agent_app_name,
        user_id="web_user"
    )
    
    # Run the workflow graph nodes asynchronously and capture the output state
    session_id = None
    async for event in runner.run_async(
        user_id="web_user",
        session_id=session.id,
        new_message=types.Content(role="user", parts=[types.Part.from_text(text=json.dumps(payload))]),
    ):
        if event.actions and event.actions.state_delta:
            state_delta = event.actions.state_delta
            if "session_id" in state_delta:
                session_id = state_delta["session_id"]
                
    if not session_id:
        from fastapi import HTTPException
        raise HTTPException(status_code=500, detail="The agent workflow failed to initialize the workspace.")
        
    return {
        "status": "success",
        "session_id": session_id
    }

