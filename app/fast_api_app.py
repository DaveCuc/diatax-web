# =========================================================================
# Resilient Logging System Setup
# =========================================================================
# Standardizes logging outputs. If Google Cloud default authentication
# project credentials are not available locally (common during development),
# it gracefully falls back to the native Python logger to prevent startup crashes.
# =========================================================================
import logging

class FallbackLogger:
    def __init__(self, name):
        self.logger = logging.getLogger(name)
        logging.basicConfig(level=logging.INFO)
        
    def log_struct(self, info, severity="INFO"):
        self.logger.info(f"[{severity}] {json.dumps(info)}")

try:
    _, project_id = google.auth.default()
    logging_client = google_cloud_logging.Client()
    logger = logging_client.logger(__name__)
except Exception:
    logger = FallbackLogger(__name__)

